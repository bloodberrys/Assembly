
STR_DSP MACRO STRING
MOV AH, 09H
MOV DX, OFFSET STRING
INT 21H
ENDM

.MODEL SMALL
.STACK 100H
.DATA
STR1 DB 21 DUP(?)
STR2 DB 21 DUP(?)
ENTER DB 10,13,"$"
MSG1 DB "ENTER FIRST STRING [20 char]: ","$"
MSG2 DB "ENTER SECOND STRING [20 char]: ","$"
SAME DB "BOTH STRINGS ARE SAME.","$"
NSAME DB "STRINGS ARE NOT SAME.","$" 

.CODE
MOV AX, @DATA
MOV DS, AX

MOV DX, OFFSET MSG1
MOV AH, 09H
INT 21H
MOV SI, OFFSET STR1
MOV CX, 20

INPUT1:
MOV AH, 01H
INT 21H
MOV [SI], AL
INC SI
CMP AL,13
JE OUT1
LOOP INPUT1   

OUT1:
MOV DL, "$"
MOV [SI], DL
MOV DX, OFFSET ENTER
MOV AH, 09H
INT 21H 

MOV DX, OFFSET MSG2
MOV AH, 09H
INT 21H 
MOV DI, OFFSET STR2
MOV CX, 20 

INPUT2:

MOV AH, 01H
INT 21H
MOV [DI], AL
INC DI
CMP AL,13
JE OUT2
LOOP INPUT2 

OUT2:
MOV DL, "$"
MOV [DI], DL
MOV DX, OFFSET ENTER
MOV AH, 09H
INT 21H 
MOV DI, OFFSET STR2
MOV SI, OFFSET STR1
MOV CX, 20   

COMPARE:
MOV AL, [DI]
MOV BL, [SI]
CMP AL, BL
JNE NS_PROC
INC DI
INC SI
LOOP COMPARE
MOV DX, OFFSET SAME
MOV AH, 09H
INT 21H
JMP END_PROC
NS_PROC:
MOV DX, OFFSET NSAME
MOV AH, 09H
INT 21H 
END_PROC:
MOV AH, 04CH
INT 21H
END




























       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
