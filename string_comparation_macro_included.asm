


TAMPIL MACRO STRING
MOV AH, 09H
MOV DX, OFFSET STRING
INT 21H
ENDM


.MODEL SMALL
.STACK 100H
.DATA
STR1 DB 21 DUP(?)
STR2 DB 21 DUP(?)
ENTER DB 10,13,"$"
MSG1 DB "ENTER 1ST STRING[MAX 20 CHAR]: ","$"
MSG2 DB "ENTER 2ND STRING[MAX 20 CHAR]: ","$" 
SAME DB " BOTH STRINGS ARE SAME.","$"
NSAME DB " STRINGS ARE NOT SAME.","$"
.CODE


MOV AX, @DATA
MOV DS, AX
TAMPIL MSG1
MOV SI, OFFSET STR1
MOV CX, 20

INPUT1:
MOV AH, 01H
INT 21H
MOV [SI], AL
INC SI
CMP AL, 13
JE OUT1
LOOP INPUT1

OUT1:

MOV DL, "$"
MOV [SI], DL
TAMPIL ENTER
TAMPIL MSG2
MOV DI, OFFSET STR2
MOV CX, 2O

INPUT2:
MOV AH, 01H
INT 21H
MOV [DI], AL
INC DI
CMP AL, 13
JE OUT2
LOOP INPUT2

OUT2:
MOV DL, "$"
MOV [DI], DL
TAMPIL ENTER
MOV DI, OFFSET STR2
MOV SI, OFFSET STR1
MOV CX, 20

COMPARE:
MOV AL, [DI]
MOV BL, [SI]
CMP AL, BL
JNE NS_PROC
INC DI
INC SI
LOOP COMPARE

TAMPIL SAME
JMP END_PROC

NS_PROC:
TAMPIL NSAME
END_PROC:
MOV AH, 04CH
INT 21H
END
